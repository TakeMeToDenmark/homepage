		<article class="article wrapper">
			<h3 id="applying">Applying</h3>

			<p>All applications are handled through <a href="http://www.optagelse.dk/"><strong>optagelse.dk</strong></a>, the website is user-friendly and offers clear instructions for the required steps. Once you create your account and login, there are four major parts of your application:</p>

			<ul>
				<li><em>Information about you</em> - basic personal information (<strong>you can&#8217;t edit this section after you&#8217;ve sent your first application</strong>)</li>
				<li><em>Entry qualifications</em> - on which bases are you applying - high school or other (<strong>you can&#8217;t edit this section after you&#8217;ve sent your first application</strong>)</li>
				<li><em>Choice of education program</em> - up to 8 applications in order of priority (<strong>you can change priorities after applying</strong>)</li>
				<li><em>Enclosures</em> - place for uploading all the scanned copies and other documentation (<strong>you can upload missing or other supplementary material after applying</strong>)</li>
			</ul>

			<p>The website will guide you through the whole application process, but there is still one thing worth pointing out. Bear in mind that once you approve the application for education program on the website <em>&#8220;You must print, sign and send a signature page for each education program&#8221;</em>. This applies to each and every program individually, meaning 3 programs at 1 school equal 3 signature pages, therefore you send all 3 of them to the same school. You usually send this sorts of documents by ordinary post, but many schools accept a scanned copy sent via email (<strong>check with your school!</strong>). The school can&#8217;t access your profile without receiving the signed page first.</p>

			<p><strong>IMPORTANT!</strong> You must make sure that the signed signature page reaches the school <strong>BEFORE</strong> the application deadline.</p>

			<p>Most schools don&#8217;t require physical copies of supporting documentation and are fine with a scanned copy uploaded through <em>Enclosures</em> section of the application website; but <strong>double check</strong> that with the website of the school(s) you are applying to.</p>

		</article>

		<article class="article wrapper">
			<h3 id="afterapplying">After applying</h3>
			<p>The response times depend on entry requirements of your school and on additional enclosures to be sent after the deadline (high school diploma). You may get a letter of acceptance sooner but generally speaking expect the answer around summer time (June/July/August).</p>
			<p>While you are impatiently waiting, you can slowly (quickly if you are super excited about moving out) start looking around for living and other moving arrangements. If you plan on staying in student housing facility then you should start signing up on waiting lists <strong>NOW!</strong></p>
